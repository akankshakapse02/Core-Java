package com.jbki;

public class Language {
	public static void main(String[] args) {
		int lng = 2;
		switch (lng) {
		case 1:
			System.out.println("java");
			break;

		case 2:
			System.out.println("python");
			break;

		case 3:
			System.out.println("javascript");
			break;

		case 4:
			System.out.println("C++");
			break;

		default:
			System.out.println("invalid input");

		}

	}

}
