package com.PractiseSession;

public class Digitname {
	public static void main(String[] args) {
		
		int num = 4;
		switch (num) {
		case 0:
			System.out.println("Zero--->1");
			break;
		case 1:
			System.out.println("One--->1");
			break;

		case 2:
			System.out.println("two----->2");
			break;

		case 3:

			System.out.println("Three---->3");
			break;

		case 4:

			System.out.println("four---->4");
			break;

		case 5:

			System.out.println("five---->5");
			break;
		case 6:

			System.out.println("six---->5");
			break;
		case 7:

			System.out.println("seven--->5");
			break;
		case 8:

			System.out.println("eight---->5");
			break;
		case 9:

			System.out.println("nine---->5");
			break;

		default:
			System.out.println("invalid input");

		}
		
	}

}
